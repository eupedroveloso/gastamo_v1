export function ViewToggle({
  value,
  onChange,
}: {
  value: ViewMode
  onChange: (mode: ViewMode) => void
}) {
  return (
    <div
      className="inline-flex items-center gap-2 rounded-full bg-g-bg p-1"
      role="tablist"
      aria-label="Visualização"
    >
      {OPTIONS.map(({ mode, Icon }) => {
        const isActive = value === mode
        return (
          <button
            key={mode}
            type="button"
            role="tab"
            aria-selected={isActive}
            onClick={() => onChange(mode)}
            className={`flex h-[26px] w-[26px] items-center justify-center rounded-full transition-colors ${
              isActive
                ? "bg-white text-g-green"
                : "text-g-muted hover:text-g-green-text"
            }`}
          >
            <Icon />
          </button>
        )
      })}
    </div>
  )
}
